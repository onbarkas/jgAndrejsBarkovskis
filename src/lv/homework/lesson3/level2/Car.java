class Car {
    private String color, manufacturer;
    private int year;

    String getColor() {
        return color;
    }

    void setColor(String color) {
        this.color = color;
    }

    String getManufacturer() {
        return manufacturer;
    }

    void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    int getYear() {
        return year;
    }

    void setYear(int year) {
        this.year = year;
    }
}
